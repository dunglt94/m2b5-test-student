public class Student {
    private String name = "John";
    private String classes = "C02";

    public Student() {}

    public String getName() {
        return name;
    }
    public void setName(String newName) {
        name = newName;
    }
    public String getClasses() {
        return classes;
    }
    public void setClasses(String newClasses) {
        classes = newClasses;
    }
}
